/**
 * 카테고리별 퀴즈 문제 반환
 * @param {string} category - 카테고리명 (예: 'doctrine', 'exercise_types')
 * @returns {Array} 해당 카테고리 문제 배열
 */
function HSEEP_getQuizByCategory(category) {
  return HSEEP_QUIZ_QUESTIONS[category] || [];
}

/**
 * 모든 퀴즈 문제 배열로 반환
 * @returns {Array} 전체 문제 배열 (순서 무작위)
 */
function HSEEP_getAllQuizQuestions() {
  const all = [];
  Object.values(HSEEP_QUIZ_QUESTIONS).forEach(catQuestions => {
    all.push(...catQuestions);
  });
  return all;
}

/**
 * 난이도별 퀴즈 문제 반환
 * @param {string} difficulty - 'easy', 'normal', 'hard'
 * @returns {Array}
 */
function HSEEP_getQuizByDifficulty(difficulty) {
  return HSEEP_getAllQuizQuestions().filter(q => q.diff === difficulty);
}

/**
 * 시나리오 ID로 시나리오 반환
 * @param {string} id - 시나리오 ID
 * @returns {Object}
 */
function HSEEP_getScenarioById(id) {
  return HSEEP_SCENARIOS.find(s => s.id === id);
}

/**
 * 챕터 번호로 캠페인 챕터 반환
 * @param {number} chapterNum - 챕터 번호 (1-10)
 * @returns {Object}
 */
function HSEEP_getCampaignChapter(chapterNum) {
  return HSEEP_CAMPAIGN.chapters.find(c => c.id === chapterNum);
}

/**
 * 퀴즈 총 문제 수 반환
 * @returns {number}
 */
function HSEEP_getTotalQuestionCount() {
  return Object.values(HSEEP_QUIZ_QUESTIONS).reduce((sum, cat) => sum + cat.length, 0);
}

